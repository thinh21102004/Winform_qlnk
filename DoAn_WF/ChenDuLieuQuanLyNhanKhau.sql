GO
SELECT 
 thanhVien.CCCDID
,thanhVien.Ho
,thanhVien.Ten
,thanhVien.Tuoi 
FROM 
ThanhVien thanhVien 
WHERE 
thanhVien.Tuoi > 50


-- Cau 2: In ra so tuoi trung binh cua tung khu pho
GO
SELECT 
       khuPho.TenKhuPho
	  ,SUM
	  (
	  TuoiTrungBinhMoiHo.TuoiTrungBinhMoiHo
	  )
	  /COUNT
	  (
	  khuPho.MaKhuPhoID
	  ) 
	  AS 
	  TuoiTrungBinhMoiKhuPho
FROM 
KhuPho khuPho
JOIN
(
SELECT 
 hoGiaDinh.MaKhuPhoID
,hoGiaDinh.MaHoID
,SUM
(
thanhVien.Tuoi
)
/COUNT
(
thanhVien.CCCDID
) 
AS 
TuoiTrungBinhMoiHo
FROM 
 ThanhVien thanhVien
,HoGiaDinh hoGiaDinh
WHERE 
thanhVien.MaHoID  = hoGiaDinh.MaHoID
GROUP BY 
 hoGiaDinh.MaHoID
,hoGiaDinh.MaKhuPhoID) 
AS 
TuoiTrungBinhMoiHo
ON 
TuoiTrungBinhMoiHo.MaKhuPhoID = khuPho.MaKhuPhoID
GROUP BY 
khuPho.TenKhuPho


-- Cau 3: Cho biết mã, tên phường và mã, họ tên người quản lý của từng phường
GO
SELECT 
       SoLuongThanhVienMoiPhuong.MaPhuongID
      ,SoLuongThanhVienMoiPhuong.TenPhuong
	  ,SoLuongThanhVienMoiPhuong.CCCDID,thanhVien.Ho
	  ,thanhVien.Ten
	  ,SoLuongThanhVienMoiPhuong.SoLuongThanhVienMoiPhuong
FROM  
ThanhVien thanhVien
join
(
SELECT 
        phuong.MaPhuongID
       ,phuong.TenPhuong, phuong.CCCDID
	   ,SUM
	   (
	   SoLuongThanhVienMoiPhuong.SoLuongThanhVienMoiPhuong
	   ) 
	   AS  
	   SoLuongThanhVienMoiPhuong
FROM 
Phuong phuong
JOIN
(
SELECT 
khuPho.MaPhuongID 
       ,SUM
	   (
	   SoLuongThanhVienMoiHoHoGiaDinh.SoLuongThanhVienMoiKhuPho
	   ) 
	   AS  
	   SoLuongThanhVienMoiPhuong
FROM 
KhuPho khuPho
JOIN
(
SELECT hoGiaDinh.MaKhuPhoID
       ,SUM 
	   (
	   SoLuongThanhVienMoiHo.SoLuongThanhVienMoiHo
	   ) 
	   AS  
	   SoLuongThanhVienMoiKhuPho
FROM 
HoGiaDinh hoGiaDinh
JOIN
(
SELECT thanhVien.MaHoID
       ,COUNT 
	   (
	   thanhVien.CCCDID
	   ) 
	   AS  
	   SoLuongThanhVienMoiHo
FROM 
ThanhVien thanhVien
GROUP BY 
MaHoID
) 
AS  
SoLuongThanhVienMoiHo
ON 
SoLuongThanhVienMoiHo.MaHoID = hoGiaDinh.MaHoID
GROUP BY 
hoGiaDinh.MaKhuPhoID
) 
AS  
SoLuongThanhVienMoiHoHoGiaDinh
ON 
SoLuongThanhVienMoiHoHoGiaDinh.MaKhuPhoID = khuPho.MaKhuPhoID
GROUP BY 
khuPho.MaPhuongID
) 
AS  
SoLuongThanhVienMoiPhuong
ON 
SoLuongThanhVienMoiPhuong.MaPhuongID = phuong.MaPhuongID
GROUP BY 
phuong.MaPhuongID, phuong.TenPhuong, phuong.CCCDID
) 
AS 
SoLuongThanhVienMoiPhuong
ON 
SoLuongThanhVienMoiPhuong.CCCDID = thanhVien.CCCDID


